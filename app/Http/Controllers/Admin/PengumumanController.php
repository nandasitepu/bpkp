    Public function getPengumuman(Request $request)
     {
       $search = $request->search;

       $pengumuman = Pengumuman::where('judul', 'LIKE', "%$search%")->orderBy('id', 'Asc')->paginate(20);

       return response()->json([
         'pengumuman' => $pengumuman
       ]);
     }

